    private void btnreservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreservarActionPerformed
        // TODO add your handling code here:
        
        Reserva dts = new Reserva();
        fReserva func = new fReserva();
        
        Calendar inicio, fin;
        int d,m,a;

        dts.setIdHabitacion(busqueda.idHabitacion);
        dts.setCliente(menu.usuario);
        
        inicio = dcfecha_ingreso.getCalendar();
        d = inicio.get(Calendar.DAY_OF_MONTH);
        m = inicio.get(Calendar.MONTH);
        a = inicio.get(Calendar.YEAR)-1900;
        dts.setFecha_ingreso(new Date(a,m,d));
        
        fin = dcfecha_salida.getCalendar();
        d = fin.get(Calendar.DAY_OF_MONTH);
        m = fin.get(Calendar.MONTH);
        a = fin.get(Calendar.YEAR)-1900;
        dts.setFecha_salida(new Date(a,m,d));
        
        double c = costoR();
        dts.setCosto_reserva(c);
        
        String estado = "Alquilada";
        dts.setEstado(estado);
        
        if (func.insertar(dts)) {
            JOptionPane.showMessageDialog(rootPane, "La Reserva Fue Registrada Satisfactoriamente");

        } else {
            JOptionPane.showMessageDialog(rootPane, "Error Al Hacer La Reserva De La Habitación");
        }
    }//GEN-LAST:event_btnreservarActionPerformed

