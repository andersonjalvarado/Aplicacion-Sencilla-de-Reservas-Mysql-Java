    public double costoR (){
        Calendar inicio, fin;
        fHabitacion func = new fHabitacion();
        int dias;
        long finMS, inicioMS;
        double costDia, costoReserva;
        
        inicio = dcfecha_ingreso.getCalendar();
        fin = dcfecha_salida.getCalendar();
        
        inicio.set(Calendar.HOUR,0);
        inicio.set(Calendar.HOUR_OF_DAY,0);
        inicio.set(Calendar.MINUTE,0);
        inicio.set(Calendar.SECOND,0);
        
        fin.set(Calendar.HOUR,0);
        fin.set(Calendar.HOUR_OF_DAY,0);
        fin.set(Calendar.MINUTE,0);
        fin.set(Calendar.SECOND,0);
        
        inicioMS = inicio.getTimeInMillis();
        finMS = fin.getTimeInMillis();
        
        dias = (int)(((Math.abs(finMS-inicioMS))/(1000*60*60*24))+1);
        costDia = func.costoDia(busqueda.idHabitacion);
        costoReserva = costDia*dias;
        txtcosto_reserva.setText("$"+ Double.toString(costoReserva));
        return costoReserva;
    }
