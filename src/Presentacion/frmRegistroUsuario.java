    private void btnregistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarseActionPerformed
        // TODO add your handling code here:
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre");
            txtnombre.requestFocus();
            return;
        }
        if (txtapellidop.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu Primer Apellido");
            txtapellidop.requestFocus();
            return;
        }
        if (txtapellidom.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu Segundo Apellido");
            txtapellidom.requestFocus();
            return;
        }
        if (txtcorreo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu correo electrÃ³nico");
            txtcorreo.requestFocus();
            return;
        }
        if (txtusuario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu usuario");
            txtusuario.requestFocus();
            return;
        }
        if (txtcontrasenia.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar tu contraseÃ±a");
            txtcontrasenia.requestFocus();
            return;
        }
        
        Persona dts = new Persona();
        fPersona func = new fPersona();
        frmMenuPrincipal menu = new frmMenuPrincipal();
        
        dts.setNombre(txtnombre.getText());
        dts.setApaterno(txtapellidop.getText());
        dts.setAmaterno(txtapellidom.getText());
        dts.setEmail(txtcorreo.getText());
        dts.setUsuario(txtusuario.getText());
        dts.setPassword(txtcontrasenia.getText());
        
        if (func.insertar(dts)) {
            menu.usuario = dts.getUsuario();
            JOptionPane.showMessageDialog(rootPane, "El usuario "+ menu.usuario +" fue registrado satisfactoriamente");
            setVisible(false);
            new frmInicioSesion().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error al registrar al usuario");
        }
    }//GEN-LAST:event_btnregistrarseActionPerformed

